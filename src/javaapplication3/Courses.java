package javaapplication3;

/**
 *
 * @author Abdulrahman
 */
public class Courses {
    private String title;
    private int type;

    public Courses(String title, int type) {
        this.title = title;
        this.type = type;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
    
}
